import { toast } from 'react-toastify';

export const NotifToastBerhasil = (name) => {
  toast.success(name, {
    position: 'top-right',
    autoClose: 5000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: 'light',
  });
};

